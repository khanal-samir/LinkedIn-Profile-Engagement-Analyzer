export function validateLinkedInProfileUrl(url: string | undefined) {
  if (!url) {
    console.error("Error: LinkedIn profile URL is required.");
    console.error(
      'Usage: npm run start "https://www.linkedin.com/in/target-profile/"'
    );
    process.exit(1);
  }

  if (!url.includes("linkedin.com/in/")) {
    console.error("Error: Invalid LinkedIn profile URL format.");
    console.error(
      "The URL must contain 'linkedin.com/in/'. Example: 'https://www.linkedin.com/in/john-doe/'"
    );
    console.error(
      'Usage: npm run start "https://www.linkedin.com/in/target-profile/"'
    );
    process.exit(1);
  }
}

